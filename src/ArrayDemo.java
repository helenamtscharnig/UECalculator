public class ArrayDemo {

    public static void main(String[] args) {
        int[] arr = new int [3];
        int[] arr2 = new int [3];

        arr[0] = 10;
        arr[1] = 2;
        arr[2] = 4;

        arr2[0] = 1;
        arr2[1] = 3;
        arr2[2] = 5;

        System.out.println(arr[0]*arr2[0]);
        System.out.println(arr[1]*arr2[1]);
        System.out.println(arr[2]*arr2[2]);

    }
}
